<script lang="ts">
import Vue from 'vue'
import { FoodItem, TYPES } from '@/models/foodItem'
import { Nutrient } from '@/models/nutrient'
import { NUTRIENTS } from '@/models/nutrient/constants'
import { NUTRIENT_BASIS } from '@/models/nutrientBasis/constants'
import { FirebaseHelper } from '@/plugins/firebase'

export default Vue.extend({
  name: 'PagesFoodItemsId',
  data(): {
    foodItem: FoodItem | null
    isEditing: boolean
    postItem: {
      name?: string
      description?: string
      images?: string[]
      keywords?: string[]
      nutrients?: Nutrient[]
      type?: string
    }
    rate: number
  } {
    return {
      foodItem: null,
      isEditing: false,
      postItem: {},
      rate: 100,
    }
  },
  computed: {
    foodItemTypes() {
      return TYPES
    },
    id() {
      return this.$route.params.id
    },
    isEditable(): boolean {
      return this.$store.state.isSignin && !this.isEditing
    },
    isNew(): boolean {
      return this.id === 'new'
    },
  },
  created() {
    this.fetchFoodItem()
  },
  methods: {
    async fetchFoodItem() {
      const db = this.$fire.firestore
      if (this.isNew) {
        this.isEditing = true
        this.foodItem = new FoodItem()
      } else {
        try {
          const doc = await FirebaseHelper.fetchItem('foodItems', this.id)
          const data = doc.data()
          this.foodItem = new FoodItem(this.id, data)
        } catch (_) {}
      }
    },
    submit() {
      if (!this.foodItem) return
      if (this.isNew) {
        this.create()
      } else {
        this.update()
      }
    },
    async create() {
      try {
        const doc = await FirebaseHelper.create('foodItems', this.postItem)
        this.$router.push({
          name: 'foodItems-id',
          params: { id: doc.id },
        })
      } catch (_) {}
    },
    update() {
      if (!this.foodItem) return
      try {
        FirebaseHelper.update('foodItems', this.foodItem?.id, this.postItem)
        console.log('Item successfully updated! üçÖ')
        this.fetchFoodItem()
        this.isEditing = false
      } catch (_) {}
    },
    onCancel() {
      this.isEditing = false
      this.fetchFoodItem()
    },
    onInput(value: string, key: string) {
      if (!this.foodItem) return
      this.$set(this.postItem, key, value)
      this.foodItem[key] = value
    },
    onNutrientValueInput(value: string, index: number) {
      const SEPARATORS = /\n|\t|,/
      const values = value.split(SEPARATORS).map((v) => {
        const number = Number(v)
        return isNaN(number) ? '' : number
      })
      const changedNutrients = values.map((value, i) => {
        return {
          nutrientId: Object.keys(NUTRIENTS)[index + i],
          value,
        }
      })

      const postNutrients = this.postItem.nutrients || [
        ...(this.foodItem?.nutrients || []),
      ]
      postNutrients.splice(index, values.length, ...changedNutrients)
      this.$set(this.postItem, 'nutrients', postNutrients)
      this.foodItem?.nutrients.forEach((n, i) => {
        n.value = postNutrients[i].value
      })
    },
    onTextArrayInput(payload: { value: string; index: -1 }, key: string) {
      if (!this.foodItem || !this.foodItem[key]) return
      if (payload.index > -1) {
        if (payload.value) {
          ;(this.foodItem[key] as string[]).splice(
            payload.index,
            1,
            payload.value
          )
        } else {
          ;(this.foodItem[key] as string[]).splice(payload.index, 1)
        }
      } else if (payload.value) {
        ;(this.foodItem[key] as string[]).push(payload.value)
      }
      this.$set(this.postItem, key, this.foodItem?.keywords)
    },
    nutrientBasis(nutrient: Nutrient) {
      const nutrientBasis = NUTRIENT_BASIS.find(
        (n) => n.nutrientID === nutrient.nutrientId
      )
      return nutrientBasis ? nutrientBasis.DietaryReferenceIntake : 100
    },
  },
})
</script>

