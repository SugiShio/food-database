<script lang="ts">
import Vue from 'vue'
import { FoodItem, TYPES } from '@/models/foodItem'
import { getFirestoreFormat } from '@/utils/firestore'

export default Vue.extend({
  name: 'PagesFoodItemsId',
  data(): { foodItem: FoodItem | null; isEditing: boolean; rate: number } {
    return {
      foodItem: null,
      isEditing: false,
      rate: 100,
    }
  },
  computed: {
    foodItemTypes() {
      return TYPES
    },
    id() {
      return this.$route.params.id
    },
    isEditable() {
      return this.$store.state.isSignin && !this.isEditing
    },
    isNew() {
      return this.id === 'new'
    },
  },
  created() {
    this.fetchFoodItem()
  },
  methods: {
    async fetchFoodItem() {
      const db = this.$fire.firestore
      if (this.isNew) {
        this.isEditing = true
        this.foodItem = new FoodItem()
      } else {
        db.collection('foodItems')
          .doc(this.id)
          .get()
          .then((doc) => {
            const data = doc.data()
            if (data) this.foodItem = new FoodItem(this.id, data)
          })
      }
    },
    submit() {
      if (!this.foodItem) return
      if (this.isNew) {
        this.create()
      } else {
        this.update()
      }
    },
    create() {
      const db = this.$fire.firestore
      const data = getFirestoreFormat(this.foodItem)
      db.collection('foodItems')
        .add(data)
        .then((doc) => {
          this.$router.push({
            name: 'foodItems-id',
            params: { id: doc.id },
          })
        })
    },
    update() {
      const db = this.$fire.firestore
      const data = getFirestoreFormat(this.foodItem)
      db.collection('foodItems')
        .doc(this.id)
        .update(data)
        .then(() => {
          console.log('Item successfully updated! üçÖ')
          this.fetchFoodItem()
          this.isEditing = false
        })
    },
    onCancel() {
      this.isEditing = false
      this.fetchFoodItem()
    },
    onNutrientValueInput(value: string, index: number) {
      const SEPARATORS = /\n|\t|,/
      const isMultipleValues = SEPARATORS.test(value)
      if (isMultipleValues) {
        const values = value.split(SEPARATORS)
        values.forEach((value, i) => {
          if (this.foodItem?.nutrients[index + i]) {
            this.foodItem?.nutrients[index + i].value = value
          }
        })
      }
    },
  },
})
</script>

